/*********************** 重要说明 ************************
 *
 * 1. 这个文件实现了一个通用的按键控制系统，支持消抖、长按、重复触发等功能。
 * 2. 按键事件通过事件队列管理，避免事件丢失。
 * 3. 提供了与菜单系统集成的接口，可以直接在菜单回调中处理按键事件。
 * 4. 所有时间参数（消抖时间、长按时间、重复间隔）都可以运行时调整。
 * 5. 按键映射表可以动态修改，支持不同的按键布局。
 *******************************************************/
#include "key_control.h"
#include "menu_system.h"

void key_menu_loop(uint32_t current_tick) {
    // 1. 扫描按键
    key_control_scan(current_tick);

    // 2. 处理所有按键事件
    menu_handle_key_event();
}
